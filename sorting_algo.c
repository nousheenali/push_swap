void	sort_array(int *num, int len)
{
	long	tmp;
	int		i;

	tmp = 0;
	i = 0;
	while (i < len - 1)
	{
		if (num[i] <= num[i + 1])
			i++;
		else
		{
			tmp = num[i];
			num[i] = num[i + 1];
			num[i + 1] = tmp;
			i = 0;
		}
	}
}
